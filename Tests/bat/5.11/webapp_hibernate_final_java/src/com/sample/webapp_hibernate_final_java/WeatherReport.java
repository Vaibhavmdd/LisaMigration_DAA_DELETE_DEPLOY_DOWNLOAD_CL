package com.sample.webapp_hibernate_final_java;

public class WeatherReport {
	private String city;
	private String state;
	private String zip;
	private String forecast;
	private String high;
	private String low;
    private long id;

    /**
     * Get the primary key associated with this report object. Hibernate will automatically generate this id.
     * @return the id.
     */
    public long getId() {
        return id;
    }

    /**
     * Set the primary key associated with this report object. Hibernate will automatically generate this id.
     * @param id the id to set
     */
    protected void setId(long id) {
        this.id = id;
    }
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getForecast() {
		return forecast;
	}
	public void setForecast(String forecast) {
		this.forecast = forecast;
	}
	public String getHigh() {
		return high;
	}
	public void setHigh(String high) {
		this.high = high;
	}
	public String getLow() {
		return low;
	}
	public void setLow(String low) {
		this.low = low;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
}
